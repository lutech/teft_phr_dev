                <a class="accordion-toggle collapsed" role="heading" data-toggle="collapse" data-parent="#accordion-programs" href="#programIndLiving">Independent Living (IL) Waiver<span class="panel-arrow"></span></a>
            </h3>
        </div>
        <div id="programIndLiving" class="accordion-body collapse">
            <div class="accordion-inner">
                <p>
                    The Independent Living Waiver is a home and community-based program that provides services to individuals with the most severe orthopedic and/or neurological
                    impairments.  This program is operated by the Mississippi Department of Rehabilitation in partnership with the Division of Medicaid.  Individuals receiving
                    services through this waiver must be Medicaid eligible, require nursing facility level of care, be medically stable, be 16 years of age or older and be
                    able to express their needs, either verbally or nonverbally, to those involved in their care.  Available services through this program include: Case Management,
                    Personal Care Attendant Services, Specialized Medical Equipment and Supplies, Home Modifications, and Transition Assistance.
                </p>
                <p>
                    Click <a href="https://www.medicaid.ms.gov/wp-content/uploads/2014/03/HCBS-Independent-Living.pdf" target="_blank">here</a> to download a pamphlet of information,
                    including How to Apply, for the Independent Living (IL) Waiver Program.
                </p>
