                <a class="accordion-toggle collapsed" role="heading" data-toggle="collapse" data-parent="#accordion-programs" href="#programAssistedLiving">Assisted Living (AL) Waiver<span class="panel-arrow"></span></a>
            </h3>
        </div>
        <div id="programAssistedLiving" class="accordion-body collapse">
            <div class="accordion-inner">
                <p>
                    The Assisted Living Waiver is a statewide program operated and administered by the Division of Medicaid. It is designed to offer assistance to qualified
                    Medicaid beneficiaries. This program allows qualified individuals to reside in a licensed Assisted Living Facility as an alternative to nursing facility
                    care or other institutional care. Persons must require nursing facility level of care, be at least 21 years of age, and Medicaid eligible. In addition
                    to this, persons qualified for the Traumatic Brain Injury Residential Facility setting must have a diagnosis of acquired traumatic brain injury, must
                    be in a family/participant crisis or have behavioral issues requiring 24 hour supervision, and must have completed acute rehabilitation treatment. Under
                    this waiver, participants must contribute to the facility for their room and board. Medicaid will pay a flat fee for services received in the facility.
                    Some of these include Personal Care Services, Homemaker Services, Attendant Care Services, Medication Oversight, Medication Administration,
                    Intermittent Skilled Nursing Services, Transportation, Attendant Call Systems, as well as Therapeutic, Social, and Recreational Programming.
                </p>
                <p>
                    Click <a href="https://www.medicaid.ms.gov/wp-content/uploads/2014/03/HCBS-Assisted-Living.pdf" target="_blank">here</a> to download a pamphlet of information,
                    including How to Apply, for the Assisted Living (AL) Waiver Program.
                </p>
