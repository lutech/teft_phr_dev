            <h3>
                <a class="accordion-toggle collapsed" role="heading" data-toggle="collapse" data-parent="#accordion-programs" href="#programB2I">Bridge to Independence (B2I) Demonstration Grant<span class="panel-arrow"></span></a>
            </h3>
        </div>
        <div id="programB2I" class="accordion-body collapse">
            <div class="accordion-inner">
                <p>
                    Bridge to Independence is a state program designed to transition Medicaid enrollees residing in an institution to the community. The program allows qualified
                    individuals to transition into the community with the help of transition services. Transition services are available in addition to appropriate waiver
                    or Community Mental Health Center services.
                </p>
                <p>
                    B2I is a federal Money Follows the Person demonstration grant funded in partnership by the U.S. Department of Health and Human Services, Centers for Medicare
                    and Medicaid Services (CMS) and the MS Division of Medicaid. Money Follows the Person is a federal initiative to help states enhance community living
                    options for persons with disabilities and establish person-driven and sustainable long-term care systems. Transition services are available in addition
                    to the appropriate waiver of Community Mental Health services.
                </p>
                <p>
                    Participants will be afforded the same level of safeguards as those available to participants enrolled in existing waivers. Persons must have resided
                    in a qualified institution for at least 90 consecutive days and received Medicaid for at least one day prior to transition from the institution into the
                    community.
                </p>
                <p>
                    In order to receive any services noted under the above waivers, approval must happen first through the Division of Medicaid.
                </p>
                <p>
                    Click <a href="/Content/Files/B2I Brochure 11 15 13.pdf" target="_blank">here</a> to download a Bridge to Independence (B2I) Demonstration Grant pamphlet
                </p>
