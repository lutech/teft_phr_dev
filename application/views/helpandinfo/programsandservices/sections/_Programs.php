                <a class="accordion-toggle collapsed" role="heading" data-toggle="collapse" data-parent="#accordion-programs" href="#programElderlyDisabled">Elderly &amp; Disabled (E&amp;D) Waiver
                    <span class="panel-arrow"></span>
                </a>
            </h3>
        </div>
        <div id="programElderlyDisabled" class="accordion-body collapse">
            <div class="accordion-inner">
                <p>
                    The Elderly and Disabled (E&amp;D) Waiver is a statewide program designed to offer assistance to qualified Medicaid beneficiaries. The program allows qualified
                    individuals to remain in a home or community based setting as an alternative to nursing facility care or other institutional care.  This program is operated
                    and administered by the Division of Medicaid. Individuals receiving services through this waiver must require the same level of care as someone applying
                    for admission to a nursing facility, be Medicaid eligible, and be
                    at least 21 years of age. Services offered through this program include: Case Management, Adult Day Services, Expanded Home Health
                    Services, Home Delivered Meals, Personal Care Services, Institutional Respite and/or In-Home Respite, and Transition Assistance.
                </p>
                <p>
                    Click <a href="https://www.medicaid.ms.gov/wp-content/uploads/2014/03/HCBS-Elderly-Disabled.pdf" target="_blank">here</a> to download a pamphlet of information,
                    including How to Apply, for the Elderly &amp; Disabled (E&amp;D) Waiver Program.
                </p>
